void HUD::showHUD(Maps* map, Menu* menu) {
    for(i = 0; i < 30; i++) {
      if(i < 20) {
        print(map->cMap[i], 0, i);
      }
      if(i < 30 && firstTime == true) {
        print(battleMenu[i], 59, i);
      }
      if(i >= 20 && i < 30 && firstTime == true) {
        print(menuBox[i-20], 0, i);
      }
    }
    if(firstTime == true) {
      menu->showMenu(2, 21);
      firstTime = false;
    }
}